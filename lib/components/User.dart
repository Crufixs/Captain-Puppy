//define all the fields
class User {
  final String username;
  final String petimage;
  final String petname;
  final String breed;
  final String gender;
  final int age;
  final double weight;
  final String about;

  const User({
    required this.username,
    required this.petimage,
    required this.petname,
    required this.breed,
    required this.gender,
    required this.age,
    required this.weight,
    required this.about,
});
}