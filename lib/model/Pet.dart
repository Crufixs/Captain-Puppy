class Pet{
  final String petImage;
  final String petName;
  final String breed;
  final String gender;
  final int age;
  final double weight;
  final String about;

  Pet({
    required this.petImage,
    required this.petName,
    required this.breed,
    required this.gender,
    required this.age,
    required this.weight,
    required this.about,
  });
}